    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre Película");
        modelo.addColumn("Clasificación");
        modelo.addColumn("Género");
        modelo.addColumn("Subtitulado");
        modelo.addColumn("Idioma");
        modelo.addColumn("Precio");

        List<clsPeliculas> listaPeliculas = peliculasDAO.consultarPeliculas();
        String[] dato = new String[7];

        for (int i = 0; i < listaPeliculas.size(); i++) {
            dato[0] = Integer.toString(listaPeliculas.get(i).getIdPeliculas());
            dato[1] = listaPeliculas.get(i).getNombre();
            dato[2] = listaPeliculas.get(i).getClasificacion();
            dato[3] = listaPeliculas.get(i).getGenero();
            dato[4] = listaPeliculas.get(i).getSubtitulado();
            dato[5] = listaPeliculas.get(i).getIdioma();
            dato[6] = Double.toString(listaPeliculas.get(i).getPrecio());
            modelo.addRow(dato);
        }

        tablaPeliculas.setModel(modelo);
    }

    public void limpiarCampos() {
        nom.setText("");
        clan.setText("");
        gen.setText("");
        sub.setText("");
        idi.setText("");
        pre.setText("");
    }

    private void cargarFilaSeleccionada() {
        int fila = tablaPeliculas.getSelectedRow();
        if (fila >= 0) {
            nom.setText(tablaPeliculas.getValueAt(fila, 1).toString());
            clan.setText(tablaPeliculas.getValueAt(fila, 2).toString());
            gen.setText(tablaPeliculas.getValueAt(fila, 3).toString());
            sub.setText(tablaPeliculas.getValueAt(fila, 4).toString());
            idi.setText(tablaPeliculas.getValueAt(fila, 5).toString());
            pre.setText(tablaPeliculas.getValueAt(fila, 6).toString());
        }
    }

    private int obtenerIdSeleccionado() {
        int fila = tablaPeliculas.getSelectedRow();
        if (fila >= 0) {
            return Integer.parseInt(tablaPeliculas.getValueAt(fila, 0).toString());
        }
        return -1;
    }

    private void tablaPeliculasMouseClicked(java.awt.event.MouseEvent evt) {
        cargarFilaSeleccionada();
    }


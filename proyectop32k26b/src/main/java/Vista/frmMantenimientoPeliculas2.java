    private void InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarActionPerformed
        // TODO add your handling code here:
         try {
            clsPeliculas pelicula = new clsPeliculas();
            pelicula.setNombre(nom.getText());
            pelicula.setClasificacion(clan.getText());
            pelicula.setGenero(gen.getText());
            pelicula.setSubtitulado(sub.getText());
            pelicula.setIdioma(idi.getText());
            pelicula.setPrecio(Double.parseDouble(pre.getText()));

            int resultado = peliculasDAO.ingresarPelicula(pelicula);

            if (resultado > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Insertado correctamente");
                int idUsuario = clsUsuarioConectado.getUsuId();
                int codigoAplicacion = 7;
                    BitacoraDAO bitacoradao = new BitacoraDAO();
                   
        bitacoradao.insert(idUsuario, codigoAplicacion, "INSERTAR");
                limpiarCampos();
                llenadoDeTablas();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al insertar");
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Precio inválido");
        }
    
    }//GEN-LAST:event_InsertarActionPerformed

    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        // TODO add your handling code here:
        try {
            int id = obtenerIdSeleccionado();

            if (id == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una fila");
                return;
            }

            clsPeliculas pelicula = new clsPeliculas();
            pelicula.setIdPeliculas(id);
            pelicula.setNombre(nom.getText());
            pelicula.setClasificacion(clan.getText());
            pelicula.setGenero(gen.getText());
            pelicula.setSubtitulado(sub.getText());
            pelicula.setIdioma(idi.getText());
            pelicula.setPrecio(Double.parseDouble(pre.getText()));

            int resultado = peliculasDAO.actualizarPelicula(pelicula);

            if (resultado > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Modificado correctamente");
                int idUsuario = clsUsuarioConectado.getUsuId();
                    BitacoraDAO bitacoradao = new BitacoraDAO();
                    int codigoAplicacion = 8;
        bitacoradao.insert(idUsuario, codigoAplicacion, "MODIFICAR");
                limpiarCampos();
                llenadoDeTablas();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al modificar");
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Precio inválido");
        }
    
        
    }//GEN-LAST:event_ModificarActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        // TODO add your handling code here:
        
     int id = obtenerIdSeleccionado();

        if (id == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una fila");
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "¿Eliminar?");
        if (confirm == 0) {
            clsPeliculas pelicula = new clsPeliculas();
            pelicula.setIdPeliculas(id);

            int resultado = peliculasDAO.borrarPelicula(pelicula);

            if (resultado > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Eliminado");
                int idUsuario = clsUsuarioConectado.getUsuId();
                    BitacoraDAO bitacoradao = new BitacoraDAO();
                    int codigoAplicacion = 9;
        bitacoradao.insert(idUsuario, codigoAplicacion, "Eliminar");
                limpiarCampos();
                llenadoDeTablas();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar");
            }
        }
    
    }//GEN-LAST:event_EliminarActionPerformed

